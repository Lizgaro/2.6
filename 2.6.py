def generate_password(n):
    # Проверяем, что число находится в допустимом диапазоне
    if n < 3 or n > 20:
        return "Число должно быть в диапазоне от 3 до 20."

    result = ""  # Пароль, который будем формировать

    # Перебираем все возможные пары чисел
    for a in range(1, n):
        for b in range(a + 1, n):  # b всегда больше a, чтобы пары были уникальными
            if n % (a + b) == 0:  # Проверяем кратность
                result += str(a) + str(b)  # Добавляем пару в строку результата

    return result

# Примеры работы
print(generate_password(9))   # Ожидаемый результат: "1218273645"
print(generate_password(11))  # Ожидаемый результат: "11029384756"
print(generate_password(20))  # Ожидаемый результат: "13141911923282183731746416515614713812911"
